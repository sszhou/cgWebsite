        <h2>J.P. Lewis</h2>
        <p>John's research area is mathematical algorithms for computer graphics. In the past he has worked in academic and industrial research labs, as well as in the film industry at Industrial Light and Magic, Disney, and elsewhere. He has published in journals and conferences including SIGGRAPH,  Transactions on Graphics, and others, and has credits on a couple films including Avatar,  The Matrix sequels,  and Forrest Gump. Several of his algorithms have been adopted in the film and games industries and incorporated in commercial graphics software packages. He has also contributed programming to media design projects that have been exhibited at the SIGGRAPH, File, and elsewhere. John's current research interests include computer vision and statistical learning applied to graphics, and design and composition from a computational point of view.
